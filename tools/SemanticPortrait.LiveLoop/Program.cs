        ob.Append($"""<circle cx="{F(x)}" cy="{F(y)}" r="{F(rr)}" fill="hsl({F(n.FillH)} {F(n.FillS * 100)}% {F(n.FillL * 100)}%)" fill-opacity="0.16" stroke="{n.StrokeHex}" stroke-width="2"{(n.Dashed ? " stroke-dasharray=\"5 4\"" : "")}/><text x="{F(x)}" y="{F(y + rr + 12)}" fill="{n.StrokeHex}" text-anchor="middle">{System.Net.WebUtility.HtmlEncode(n.Label)}</text>""");
    }
    ob.Append("</svg>");
    File.WriteAllText(Path.Combine(outDir, "observatory.html"), ob.ToString());
    Console.WriteLine($"rendered: sigil seed={p.Sig.Seed} cells={p.Cells.Count} fil={p.Filaments.Count} " +
                      $"rip={p.Ripples.Count} fire={F(p.GlowWarmth)} | obs nodes={vm.Nodes.Count} linked={F(vm.LinkedFraction)}");
    local.Dispose();
}

/// <summary>Seed the app's DEV SANDBOX with the self-model from the real chat sessions (entries,
/// moods, notes, events, graph — real MiniLM embeddings) so the Observatory and the Sigil render
/// from lived data. Run with the app CLOSED: dotnet run -- seed</summary>
static async Task SeedSandbox()
{
    var dataDir = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData),
        "User Name", "com.companyname.semanticportrait.app", "Data");
    var db = new Db(Path.Combine(dataDir, "semanticportrait.db.dev"));
    db.OpenPlaintext();
    var local = new LocalEmbedder(Path.Combine(FindRepoRoot(), "models", "minilm"));
    if (!local.IsAvailable) { Console.WriteLine("!! MiniLM missing"); return; }

    async Task<long> Msg(string text, string utc, string mood, double val, double inten, double en, string topics, string people, string summary)
    {
        var id = db.AddMessage("user", text, utc);
        var v = await local.EmbedAsync(text);
        if (v is not null) db.AddEmbedding("message", id, v);
        db.SetEntryMeta(id, mood, val, inten, en, topics, people, summary);
        return id;
    }
    async Task Note(string text, string utc)
    {
        var id = db.AddNote(text, utc);
        var v = await local.EmbedAsync(text);
        if (v is not null) db.UpsertEmbedding("note", id, v);
    }
    async Task<long> Node(string cat, string label, bool inf, double conf)
    {
        var id = db.UpsertNode(cat, label, inf, conf);
        var v = await local.EmbedAsync($"{cat} {label}");
        if (v is not null) db.UpsertEmbedding("node", id, v);
        return id;
    }
    async Task Event(string when, string summary)
    {
        var id = db.AddEvent(when, summary);
        var v = await local.EmbedAsync(summary);
        if (v is not null) db.UpsertEmbedding("event", id, v);
    }
    static string D(int day, int hour) => new DateTime(2026, 3, day, hour, 0, 0, DateTimeKind.Utc).ToString("o");

    // --- the self-model from the chat sessions -----------------------------------
    var creating = await Node("fire", "creating", false, 0.95);
    var tennis = await Node("body", "tennis and HIIT routine", false, 0.9);
    var gaming = await Node("joy", "gaming", false, 0.85);
    var projects = await Node("joy", "personal projects", false, 0.9);
    var flirt = await Node("joy", "flirting/social validation", true, 0.7);
    var priya = await Node("connection", "Priya", false, 0.95);
    var partner = await Node("connection", "partner", false, 0.95);
    var mom = await Node("connection", "mom", false, 0.85);
    var sister = await Node("connection", "sister", false, 0.85);
    var thin = await Node("connection", "thin social life", false, 0.9);
    var radar = await Node("distortion", "rejection-radar", true, 0.85);
    var hopium = await Node("distortion", "fantasy re-inflation", true, 0.75);
    var pining = await Node("wound", "pining after low-interest women", false, 0.9);
    var separation = await Node("wound", "marital separation", false, 0.9);
    var critic = await Node("mind", "inner critic — irresponsible", true, 0.7);
    var avoid = await Node("self", "conversation avoidance", true, 0.75);
    var romance = await Node("heart", "romance and passion", false, 0.85);
    var family = await Node("heart", "family/partner aspiration", false, 0.8);
    var anchor = await Node("keystone", "reality-anchor", false, 0.9);
    var work = await Node("work", "computer-centered work", false, 0.9);

    void Edge(long a, long b, string t, double c = 0.8) => db.AddEdge(a, b, t, t, true, c);
    Edge(radar, pining, "amplifies"); Edge(hopium, pining, "amplifies");
    Edge(avoid, pining, "keeps-alive"); Edge(priya, pining, "prototype-of", 0.9);
    Edge(radar, creating, "steals-the-fuel", 0.7); Edge(flirt, radar, "counteracts", 0.65);

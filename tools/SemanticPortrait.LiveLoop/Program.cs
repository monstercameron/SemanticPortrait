static string FindRepoRoot()
{
    var dir = new DirectoryInfo(AppContext.BaseDirectory);
    for (int i = 0; i < 8 && dir is not null; i++, dir = dir.Parent!)
        if (Directory.Exists(Path.Combine(dir.FullName, ".git"))) return dir.FullName;
    throw new InvalidOperationException("repo root not found");
}

/// <summary>Diagnostic probe: send ONE message through the app's exact wiring (same prompt
/// assembly, same tools, same provider client) against the DEV SANDBOX, printing every event —
/// tokens, tool calls, reasoning presence, errors. For "the agent went silent" investigations.
/// dotnet run -- ask what do you know about me</summary>
static async Task AskProbe(string question)
{
    var dataDir = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData),
        "User Name", "com.companyname.semanticportrait.app", "Data");
    var db = new Db(Path.Combine(dataDir, "semanticportrait.db.dev"));
    db.OpenPlaintext();
    var usage = new UsageTracker(db);
    var cfg = new LlmConfig(db);
    var http = new HttpClient { Timeout = TimeSpan.FromMinutes(5) };
    var openai = new OpenAIClient(http, usage, cfg);
    Console.WriteLine($"model={cfg.SelectedModelId("openai")} hasKey={openai.HasKey}");

    var local = new LocalEmbedder(Path.Combine(dataDir, "models", "minilm"));
    Console.WriteLine($"local embedder available={local.IsAvailable}");
    var embedder = (IEmbedder)local;
    var profile = new ProfileTools(new ProfileStore(db, Path.GetTempFileName()));
    var recall = new RecallTools(new RecallEngine(db, embedder, new EntityResolver(db, embedder)));
    var intake = new IntakeTools(db);

    var sys = Prompts.Analyst + $"\n\n## Now\nCurrent time: {DateTime.Now:dddd, MMM d, yyyy h:mm tt}.\n" +
              "Privacy state: storage local+encrypted; replies are generated by OpenAI (CLOUD — entry text leaves this machine, masking is OFF)." +
              (intake.StatusLine() is { } il ? "\n" + il : "");
    var history = db.GetMessages().Where(m => m.Role is "user" or "assistant")
        .Select(m => new ChatMessage(m.Role == "user" ? "user" : "assistant", m.Text)).ToList();
    history.Add(new ChatMessage("user", question));
    var specs = profile.ReadSpecs.Concat(recall.MainSpecs)
        .Concat(intake.IsComplete() ? Array.Empty<object>() : intake.Specs).ToList();
    Console.WriteLine($"history={history.Count} msgs, specs={specs.Count} tools, sysPrompt={sys.Length} chars");

    async Task<string> Exec(string name, string a)
    {
        Console.WriteLine($"\n[TOOL {name}] {a}");
        var r = intake.Handles(name) ? await intake.ExecuteAsync(name, a)
            : recall.Handles(name) ? await recall.ExecuteAsync(name, a)
            : await profile.ExecuteAsync(name, a);
        Console.WriteLine($"[TOOL RESULT] {(r.Length > 300 ? r[..300] + "…" : r)}");
        return r;
    }

    var sw = System.Diagnostics.Stopwatch.StartNew();
    var total = 0;
    await foreach (var tok in openai.StreamReplyAsync(sys, history, specs, Exec,
        onReasoning: r => Console.WriteLine($"[REASONING {r.Length} chars] {(r.Length > 160 ? r[..160] + "…" : r)}"),
        onError: e => Console.WriteLine($"\n[ERROR] {e}")))
    {
        total += tok.Length;
        Console.Write(tok);
    }
    Console.WriteLine($"\n--- done: {total} chars in {sw.Elapsed.TotalSeconds:0.0}s · cost ${usage.CostUsd:0.0000} ---");
    local.Dispose();
}

/// <summary>The comparison study's implementable half: six synthetic personas → six sigils in one
/// grid, to judge whether structurally different inner worlds paint visually distinct images.
/// dotnet run -- gallery out=DIR</summary>
static void RenderGallery(string outDir)
{
    SemanticPortrait.Core.Constellation.SigilSignature Sig(int band, double density, double val, double energy,
        double vol, double contra, double fire, double inf, double[] mix) =>
        new(band, density, val, energy, vol, contra, fire, inf, mix, Seed(band, density, val, fire));
    static int Seed(int b, double d, double v, double f) => b * 1000003 + (int)(d * 97 + (v + 1) * 31 + f * 57) * 7919;

    // EmotionMix order: Anger, Disgust, Creativity, Calm, Sadness, Fear, Love, Joy
    var personas = new (string Name, SemanticPortrait.Core.Constellation.SigilSignature S)[]
    {
        ("extrovert · dense · joyful", Sig(4, 0.9, 0.5, 0.75, 0.25, 0.1, 0.4, 0.15, new[] { 0.02, 0.01, 0.15, 0.12, 0.05, 0.05, 0.25, 0.35 })),
        ("introvert · sparse · calm", Sig(2, 0.15, 0.15, 0.3, 0.1, 0.05, 0.2, 0.3, new[] { 0.02, 0.02, 0.2, 0.45, 0.1, 0.06, 0.05, 0.1 })),
        ("grieving · low · heavy", Sig(3, 0.4, -0.6, 0.25, 0.3, 0.2, 0.05, 0.2, new[] { 0.06, 0.04, 0.03, 0.05, 0.55, 0.17, 0.07, 0.03 })),
        ("volatile · conflicted", Sig(3, 0.6, -0.1, 0.6, 0.9, 0.9, 0.15, 0.45, new[] { 0.25, 0.08, 0.08, 0.04, 0.18, 0.22, 0.08, 0.07 })),
        ("builder · fire-forward", Sig(3, 0.55, 0.3, 0.7, 0.3, 0.15, 0.85, 0.25, new[] { 0.04, 0.02, 0.45, 0.1, 0.06, 0.08, 0.08, 0.17 })),
        ("new user · barely known", Sig(1, 0.05, 0.0, 0.4, 0.0, 0.0, 0.1, 0.6, new[] { 0.0, 0.0, 0.1, 0.12, 0.05, 0.08, 0.02, 0.06 })),
    };

    var html = new StringBuilder("<!doctype html><meta charset=\"utf-8\"><style>body{margin:0;background:#000;display:grid;grid-template-columns:repeat(3,1fr);}figure{margin:0;position:relative;}figcaption{position:absolute;bottom:6px;left:0;right:0;text-align:center;color:#aaa;font:12px sans-serif;}</style>");
    foreach (var (name, sig) in personas)
    {
        var p = SemanticPortrait.Core.Constellation.SigilForge.Paint(sig);
        html.Append("<figure>").Append(SigilSvg(p, 520)).Append($"<figcaption>{name} · seed {p.Sig.Seed}</figcaption></figure>");
    }
    File.WriteAllText(Path.Combine(outDir, "gallery.html"), html.ToString());
    Console.WriteLine("gallery: 6 personas → gallery.html (seeds: " +
        string.Join(", ", personas.Select(x => SemanticPortrait.Core.Constellation.SigilForge.Paint(x.S).Sig.Seed)) + ")");
}

/// <summary>One sigil as a self-contained SVG string (shared by render + gallery modes).</summary>
static string SigilSvg(SemanticPortrait.Core.Constellation.SigilPainting p, int size)
{
    string F(double v) => v.ToString("0.###", System.Globalization.CultureInfo.InvariantCulture);
    var sb = new StringBuilder();
    var uid = Math.Abs(p.Sig.Seed);
    sb.Append($"""<svg width="{size}" height="{size}" viewBox="0 0 1000 1000" style="background:radial-gradient(120% 95% at 50% 42%, hsl({F(p.BgHue)} 45% {F(p.BgLight * 100 + 6)}%) 0%, hsl({F(p.BgHue)} 40% {F(p.BgLight * 100)}%) 55%, hsl({F(p.BgHue)} 45% 2.5%) 100%)">""");
    sb.Append($"""<defs><filter id="n{uid}" x="-40%" y="-40%" width="180%" height="180%"><feTurbulence type="fractalNoise" baseFrequency="{F(p.TurbulenceFrequency)}" numOctaves="4" seed="{p.Sig.Seed & 0xffff}" result="noise"/><feDisplacementMap in="SourceGraphic" in2="noise" scale="150"/><feGaussianBlur stdDeviation="16"/></filter><filter id="s{uid}" x="-60%" y="-60%" width="220%" height="220%"><feGaussianBlur stdDeviation="10"/></filter><radialGradient id="v{uid}" cx="50%" cy="50%" r="72%"><stop offset="60%" stop-color="black" stop-opacity="0"/><stop offset="100%" stop-color="black" stop-opacity="0.55"/></radialGradient></defs>""");
    foreach (var st in p.Stars)
        sb.Append($"""<circle cx="{F(st.X * 1000)}" cy="{F(st.Y * 1000)}" r="{F(st.R)}" fill="white" opacity="{F(st.Opacity)}"/>""");
    sb.Append($"""<g filter="url(#n{uid})">""");
    foreach (var c in p.Cells)
    {
        sb.Append($"""<circle style="mix-blend-mode:screen" cx="{F(c.X * 1000)}" cy="{F(c.Y * 1000)}" r="{F(c.R * 1000)}" fill="hsl({F(c.Hue)} {F(c.Sat * 100)}% {F(c.Light * 100)}%)" opacity="{F(c.Opacity)}"/>""");
        sb.Append($"""<circle style="mix-blend-mode:screen" cx="{F(c.X * 1000)}" cy="{F(c.Y * 1000)}" r="{F(c.R * 420)}" fill="hsl({F(c.Hue)} {F(c.Sat * 100)}% {F(Math.Min(0.78, c.Light + 0.22) * 100)}%)" opacity="{F(c.Opacity * 0.9)}"/>""");
    }
    sb.Append($"""</g><g filter="url(#s{uid})">""");
    foreach (var f in p.Filaments)
        sb.Append($"""<path style="mix-blend-mode:screen" d="M {F(f.X1 * 1000)} {F(f.Y1 * 1000)} Q {F(f.CX * 1000)} {F(f.CY * 1000)} {F(f.X2 * 1000)} {F(f.Y2 * 1000)}" stroke="hsl({F(f.Hue)} 70% 70%)" stroke-width="{F(f.Width)}" fill="none" opacity="{F(f.Opacity)}"/>""");
    sb.Append("</g>");
    foreach (var r in p.Ripples)
        sb.Append($"""<g><circle cx="{F(r.X * 1000)}" cy="{F(r.Y * 1000)}" r="{F(r.R * 1000)}" stroke="hsl({F(r.HueA)} 70% 65%)" stroke-width="1.2" fill="none" opacity="0.3"/><circle cx="{F(r.X * 1000)}" cy="{F(r.Y * 1000)}" r="{F(r.R * 720)}" stroke="hsl({F(r.HueB)} 70% 65%)" stroke-width="1" fill="none" opacity="0.22"/></g>""");
    if (p.GlowWarmth > 0.03)
        sb.Append($"""<circle style="mix-blend-mode:screen" cx="500" cy="515" r="{F(70 + p.GlowWarmth * 200)}" fill="hsl(32 95% 66%)" opacity="{F(0.14 + p.GlowWarmth * 0.30)}" filter="url(#s{uid})"/>""");
    sb.Append($"""<rect width="1000" height="1000" fill="url(#v{uid})"/></svg>""");
    return sb.ToString();
}


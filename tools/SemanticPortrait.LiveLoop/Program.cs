// SemanticPortrait LiveLoop — live-fire eval of the agent pipeline against the REAL provider.
//
// AIs are nondeterministic: the deterministic replay tests prove the plumbing, this proves the
// MODEL's behavior under the current prompts. Each iteration: fresh isolated DB → run the scripted
// intake (the user answers from the real 2026-03-20 session) through the same wiring the app uses
// (main agent + clean-room analyst, all gated tools) → dump the full transcript to runs/ → grade
// deterministic INVARIANTS on the resulting store. Refine prompts, run again, compare scorecards.
//
// Usage: dotnet run --project tools/SemanticPortrait.LiveLoop [-- runs=N]
// Cost guard: aborts a run if spend exceeds $5.

using System.Text;
using SemanticPortrait.Core;

if (args.Contains("seed"))
{
    await SeedSandbox();
    return 0;
}
if (args.Contains("render"))
{
    await RenderPreviews(args.FirstOrDefault(a => a.StartsWith("out="))?[4..] ?? Path.GetTempPath());
    return 0;
}
if (args.Contains("gallery"))
{
    RenderGallery(args.FirstOrDefault(a => a.StartsWith("out="))?[4..] ?? Path.GetTempPath());
    return 0;
}
if (args.Contains("ask"))
{
    await AskProbe(string.Join(' ', args.SkipWhile(a => a != "ask").Skip(1)));
    return 0;
}
if (args.Contains("recover"))
    return Recover.Run(args.FirstOrDefault(a => a.StartsWith("data="))?[5..]);
if (args.Contains("thesis"))
{
    // Print the core Od's thesis narration from the live sandbox — layout/copy checking.
    var dataDir = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData),
        "User Name", "com.companyname.semanticportrait.app", "Data");
    var tdb = new Db(Path.Combine(dataDir, "semanticportrait.db.dev"));
    tdb.OpenPlaintext();
    var temb = new LocalEmbedder(Path.Combine(FindRepoRoot(), "models", "minilm"));
    var teng = new RecallEngine(tdb, temb, new EntityResolver(tdb, temb));
    Console.WriteLine(await teng.NarrateNodeAsync(SemanticPortrait.Core.Constellation.Encoding.SyntheticCoreId));
    tdb.Close();
    return 0;
}

var runsWanted = args.FirstOrDefault(a => a.StartsWith("runs="))?[5..] is { } n && int.TryParse(n, out var rn) ? rn : 1;
var runsDir = Path.Combine(FindRepoRoot(), "tools", "SemanticPortrait.LiveLoop", "runs");
Directory.CreateDirectory(runsDir);

var overall = new List<(int Run, int Passed, int Total, double Cost)>();
for (var run = 1; run <= runsWanted; run++)
{
    var result = await RunOnce(run, runsDir);
    overall.Add(result);
}

Console.WriteLine("\n================ SUMMARY ================");
foreach (var (run, passed, total, cost) in overall)
    Console.WriteLine($"run {run}: {passed}/{total} invariants · ${cost:0.00}");
return overall.All(o => o.Passed == o.Total) ? 0 : 1;


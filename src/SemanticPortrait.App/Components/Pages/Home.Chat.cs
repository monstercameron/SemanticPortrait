    /// <summary>The live privacy facts for the system prompt — the agent describes the privacy
    /// model from THIS, never from vibes (Prompts §Privacy honesty). Local provider = the full
    /// loop stays on-device; cloud = entry text leaves, masked or not per the user's consent.</summary>
    private string PrivacyLine()
    {
        if (Ai.ProviderId == "lmstudio")
            return $"Privacy state: storage local+encrypted; provider {Ai.DisplayName} runs LOCALLY — nothing leaves this machine.";
        var masking = Microsoft.Maui.Storage.Preferences.Default.Get("masking", false);
        return $"Privacy state: storage local+encrypted; replies are generated by {Ai.DisplayName} (CLOUD — " +
               $"entry text leaves this machine, {(masking ? "PII-masked first" : "masking is OFF")}).";
    }

    /// <summary>Have the agent post an unprompted message (reminders, the post-import welcome).
    /// Gets the read-only recall tools so a proactive message can be GROUNDED in the portrait —
    /// the post-import continuation greeting is exactly a recall-then-speak move.</summary>
    private async Task FireProactive(string kickInstruction)
    {
        if (_busy) return;
        _busy = true;
        var sys = Prompts.Analyst + $"\n\n## Now\nCurrent time: {DateTime.Now:dddd, MMM d, yyyy h:mm tt}.\n{PrivacyLine()}";
        var reply = new Msg { Role = "ai", Text = "", Streaming = true };
        _messages.Add(reply); _scrollDown = true; StateHasChanged();
        var specs = Tools.ReadSpecs.Concat(Recall.MainSpecs).ToList();
        async Task<string> Exec(string name, string args)
        {
            var result = Recall.Handles(name) ? await Recall.ExecuteAsync(name, args)
                : await Tools.ExecuteAsync(name, args);
            _messages.Remove(reply);
            AddToolBubble(FriendlyToolNote(name), $"params: {args}\nresult: {result}");
            _messages.Add(reply);
            StateHasChanged();
            return result;
        }
        var perr = new ProviderErrs();
        var clean = new System.Text.StringBuilder();
        try
        {
            using var watchdog = new System.Threading.CancellationTokenSource(TimeSpan.FromMinutes(4));
            await foreach (var tok in Ai.StreamReplyAsync(sys, new[] { new ChatMessage("user", kickInstruction) }, specs, Exec,
                onError: err =>
                {
                    if (perr.ReportAndShouldShow(err)) reply.Text += $"{(reply.Text.Length > 0 ? "\n" : "")}⚠️ {FriendlyProviderError(err)}";
                    DevTrap.Report("proactive-provider", new InvalidOperationException(err));
                },
                ct: watchdog.Token))
            {
                if (perr.IsErrToken(tok)) continue;
                reply.Text += tok; clean.Append(tok); _scrollDown = true; StateHasChanged();
            }
        }
        catch (Exception e) { DevTrap.Report("proactive-stream", e); }
        finally
        {
            reply.Streaming = false;
            if (!string.IsNullOrWhiteSpace(clean.ToString())) await Persist("assistant", clean.ToString());
            _busy = false; _focusNext = true; StateHasChanged();
        }
    }

    /// <summary>
    /// Provider-error bookkeeping for one streamed reply. The client reports an error via
    /// onError AND yields the same text inline (for bare consumers) — this UI uses onError,
    /// so the inline copy must be skipped or the error shows twice. It also keeps error text
    /// out of what gets persisted: an error stored as an assistant message becomes thread
    /// history the model re-reads (and compacts, and embeds) forever after.
    /// </summary>
    private sealed class ProviderErrs
    {
        private readonly HashSet<string> _raw = new();
        public bool Any => _raw.Count > 0;
        /// <summary>True only for the FIRST error of the stream — later ones (the provider often
        /// sends the same failure as both an `error` event and `response.failed`) stay off-screen.</summary>
        public bool ReportAndShouldShow(string err) => _raw.Add(err) && _raw.Count == 1;
        public bool IsErrToken(string tok) => _raw.Contains(tok);
    }

    /// <summary>Translate raw provider errors the user can act on into plain instructions.</summary>
    private static string FriendlyProviderError(string err) =>
        err.Contains("insufficient_quota") || err.Contains("exceeded your current quota")
            ? "your AI provider account is out of credit. Add credit (for OpenAI: platform.openai.com → Billing) " +
              "or switch provider/key in ⋯ → LLM settings, then send your message again."
            : err;

    private HandoffDeduper? _handoffDedup;   // session-scoped send_to_analysis duplicate gate
    private bool _greeting;
    /// <summary>Proactively open a brand-new thread: the agent greets and starts onboarding.</summary>
    private async Task GreetOnboarding()
    {
        if (_greeting || _busy) return;
        _greeting = true; _busy = true;

        var sys = Prompts.Analyst + $"\n\n## Now\nCurrent time: {DateTime.Now:dddd, MMM d, yyyy h:mm tt}. " +
                  $"This is a brand-new, empty thread.\n{PrivacyLine()}";
        var kick = new[] { new ChatMessage("user",
            "[A fresh thread just started. Greet me warmly in one short line, orient me in one more — " +
            "honestly, per your Privacy honesty section and the Privacy state line (continuous, never " +
            "deleted, encrypted on this machine; replies via my chosen AI provider) — then offer the " +
            "~10-minute intake per your onboarding section, including that I can say 'skip' to any " +
            "question or 'just start' to skip it all, and ask the first question (my name). Keep it " +
            "light; no wall of text.]") };

        var reply = new Msg { Role = "ai", Text = "", Streaming = true };
        await InvokeAsync(() => { _messages.Add(reply); _scrollDown = true; StateHasChanged(); });
        var perr = new ProviderErrs();
        var clean = new System.Text.StringBuilder();
        try
        {
            using var watchdog = new System.Threading.CancellationTokenSource(TimeSpan.FromMinutes(4));
            await foreach (var tok in Ai.StreamReplyAsync(sys, kick,
                onError: err =>
                {
                    if (perr.ReportAndShouldShow(err)) reply.Text += $"{(reply.Text.Length > 0 ? "\n" : "")}⚠️ {FriendlyProviderError(err)}";
                    DevTrap.Report("greet-provider", new InvalidOperationException(err));
                },
                ct: watchdog.Token))
            {
                if (perr.IsErrToken(tok)) continue;
                reply.Text += tok; clean.Append(tok); _scrollDown = true;
                await InvokeAsync(StateHasChanged);
            }
        }
        catch (Exception e) { DevTrap.Report("greet-stream", e); }
        finally
        {
            reply.Streaming = false;
            // Persist real content only — a failed greeting must not save, so the next
            // launch sees an empty thread and simply greets again.
            if (!string.IsNullOrWhiteSpace(clean.ToString())) await Persist("assistant", clean.ToString());
            _busy = false; _greeting = false; _focusNext = true;
            await InvokeAsync(StateHasChanged);
        }
    }

